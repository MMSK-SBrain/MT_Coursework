class TestCompletePipeline:
    """Test the complete end-to-end pipeline"""

    def test_complete_pipeline_single_module(
        self,
        test_config,
        sample_framework_content,
        sample_questions,
        temp_output_dir,
        temp_question_bank_dir,
        create_question_file,
        create_framework_file
    ):
        """
        Test: Parse → Transform → Load Questions → Process Images → Layout → PDF

        This is the core integration test verifying the entire pipeline works.
        """
        # 1. Create test framework file
        framework_path = temp_output_dir / "test_framework.md"
        create_framework_file(framework_path, sample_framework_content)

        # 2. Create approved questions file
        questions_path = temp_question_bank_dir["approved"] / "test-module-0.json"
        create_question_file(questions_path, sample_questions, "test-module-0")

