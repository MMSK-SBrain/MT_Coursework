class TestBatchProcessing:
    """Test batch processing workflows"""

    def test_batch_processing_multiple_modules(
        self,
        test_config,
        sample_framework_content,
        sample_questions,
        temp_output_dir,
        temp_question_bank_dir,
        create_question_file,
        create_framework_file
    ):
        """
        Test: Discover frameworks → Check review status → Batch process → Verify outputs
        """
        # Create multiple framework files
        modules = ["module-0", "module-1", "module-2"]

        for module_id in modules:
            # Create framework file
            framework_path = temp_output_dir / "frameworks" / f"{module_id}-framework.md"
            framework_path.parent.mkdir(parents=True, exist_ok=True)
            create_framework_file(framework_path, sample_framework_content)

            # Create approved questions
            questions_path = temp_question_bank_dir["approved"] / f"{module_id}.json"
            create_question_file(questions_path, sample_questions, module_id)

        test_config.config["questions"]["approved_directory"] = str(temp_question_bank_dir["approved"])
        test_config.config["global"]["output_directory"] = str(temp_output_dir)

        # Simulate batch processor
        results = {}

        for module_id in modules:
            framework_path = temp_output_dir / "frameworks" / f"{module_id}-framework.md"

            try:
                # Parse
                parser = FrameworkParser()
                parse_result = parser.parse_file(framework_path)

                if not parse_result.success:
                    results[module_id] = {"success": False, "error": "Parse failed"}
                    continue

                # Transform
                transformer = ContentTransformer(test_config)
                sections = transformer.transform(parse_result.sections)

                # Load questions
                loader = QuestionLoader(test_config)
                questions = loader.load_questions(module_id)

                # Check review status
                status = loader.get_review_status(module_id)
                if not status["review_complete"]:
                    results[module_id] = {"success": False, "blocked": True, "reason": "Unreviewed questions"}
                    continue

                # Process images
                processor = ImageProcessor(test_config)
                images = processor.process_images(parse_result.images)

                # Layout
                layout_engine = LayoutEngine(test_config)

                from src.models import HandoutDocument
                handout_doc = HandoutDocument(
                    metadata=parse_result.metadata,
                    sections=sections,
                    questions=questions,
                    images=images,
                    toc=[],
                    generation_timestamp=datetime.now(),
                    config_used=test_config.to_dict(),
                    output_path=temp_output_dir / f"{module_id}-handout.pdf",
                    warnings=[],
                    question_review_complete=True
                )

                html = layout_engine.render(handout_doc)

                # PDF
                renderer = PDFRenderer(test_config)
                pdf_path = renderer.render_handout(
                    html_content=html,
                    output_path=temp_output_dir / f"{module_id}-handout.pdf",
                    metadata=parse_result.metadata
                )

                results[module_id] = {"success": True, "pdf_path": str(pdf_path)}

            except Exception as e:
                results[module_id] = {"success": False, "error": str(e)}

        # Verify all succeeded
        assert len(results) == 3
        assert all(results[m]["success"] for m in modules)

        for module_id in modules:
            pdf_path = Path(results[module_id]["pdf_path"])
            assert pdf_path.exists()
            assert pdf_path.stat().st_size > 0

        print(f"✓ Batch processed {len(modules)} modules successfully")

